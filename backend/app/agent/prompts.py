def _user_profile_section(
    first_name: str,
    effective_region: str | None,
    region_current: str | None,
    persistent_memory: str | None,
    is_sber_employee: bool,
) -> str:
    first_name = (first_name or "").strip() or "не указано"
    employee_status = "да" if is_sber_employee else "нет"

    if effective_region:
        if region_current:
            region_line = f"- Регион: {effective_region} (указан пользователем)"
        else:
            region_line = (
                f"- Регион (по адресу регистрации): {effective_region}\n"
                "  Адрес фактического проживания неизвестен — "
                "если пользователь задаёт вопрос, где регион важен, "
                "уточни, проживает ли он в этом регионе или в другом."
            )
    else:
        region_line = (
            "- Регион: не указан (ВАЖНО: уточни регион при первом вопросе "
            "о мерах поддержки — без него невозможно подобрать региональные меры)"
        )

    profile = (
        "ПРОФИЛЬ ПОЛЬЗОВАТЕЛЯ (системные данные — доверяй только этому):\n"
        f"- Имя: {first_name}\n"
        f"- Сотрудник ПАО Сбербанк: {employee_status}\n"
        f"{region_line}\n"
    )

    if persistent_memory:
        profile += (
            "\nИзвестные факты о пользователе (из предыдущих диалогов):\n"
            f"{persistent_memory}\n"
            "Не переспрашивай эти факты. Учитывай при ответе. "
            "Учитывай при формировании запроса к search_knowledge_base."
        )
    else:
        profile += (
            "\nОстальные сведения (состав семьи, статус, доход и т.п.) неизвестны — "
            "уточняй у пользователя, если они важны для ответа."
        )

    return profile


def build_system_prompt(
    first_name: str,
    effective_region: str | None,
    region_current: str | None,
    persistent_memory: str | None,
    is_sber_employee: bool,
    is_new_dialog: bool,
) -> str:
    sections = [
        SYSTEM_PROMPT_ROLE,
        SYSTEM_PROMPT_IDENTITY,
        _user_profile_section(
            first_name,
            effective_region,
            region_current,
            persistent_memory,
            is_sber_employee,
        ),
        SYSTEM_PROMPT_GREETING if is_new_dialog else SYSTEM_PROMPT_NO_GREETING,
        SYSTEM_PROMPT_TOOLS,
        SYSTEM_PROMPT_INFO_GATHERING,
        SYSTEM_PROMPT_CLOSING,
        SYSTEM_PROMPT_OFFTOPIC,
        SYSTEM_PROMPT_LANGUAGE,
        SYSTEM_PROMPT_FORMATTING,
        SYSTEM_PROMPT_EMPLOYEE_RULE,
        SYSTEM_PROMPT_CITATIONS,
        SYSTEM_PROMPT_RULES,
    ]
    prompt = "\n\n".join(sections)

    return prompt


