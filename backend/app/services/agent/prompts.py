def _public_chunks_section(chunks: list[RetrievedChunk]) -> str:
    if not chunks:
        return ""
    payload = [
        {
            "source_name": c.source_name,
            "source_url": c.source_url,
            "text": c.text,
        }
        for c in chunks[:100]
    ]
    return (
        "\n\nПУБЛИЧНЫЕ ИСТОЧНИКИ (используй кликабельные ссылки в формате "
        "`[source_name](source_url)`):\n"
        f"{json.dumps(payload, ensure_ascii=False)}"
    )


