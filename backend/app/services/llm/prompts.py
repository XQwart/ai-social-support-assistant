def _user_profile_section(user: UserModel) -> str:
    first_name = (user.first_name or "").strip() or "не указано"
    employee_status = "да" if user.is_sber_employee else "нет"
    return (
        "ПРОФИЛЬ ПОЛЬЗОВАТЕЛЯ (известные данные):\n"
        f"- Имя: {first_name}\n"
        f"- Сотрудник ПАО Сбербанк: {employee_status}\n"
        "Остальные сведения (регион, состав семьи, статус, доход и т.п.) неизвестны — "
        "уточни их у пользователя, если они важны для ответа."
    )


def _public_chunks_section(chunks: list[RetrievedChunk]) -> str:
    if not chunks:
        return ""
    payload = [
        {
            "source_name": c.source_name,
            "source_url": c.source_url,
            "text": c.text,
        }
        for c in chunks[:100]
    ]
    return (
        "\n\nПУБЛИЧНЫЕ ИСТОЧНИКИ (используй кликабельные ссылки в формате "
        "`[source_name](source_url)`):\n"
        f"{json.dumps(payload, ensure_ascii=False)}"
    )


def _internal_chunks_section(
    chunks: list[RetrievedChunk], user_is_employee: bool
) -> str:
    if not chunks or not user_is_employee:
        return ""
    payload = [{"text": c.text} for c in chunks[:100]]
    return (
        "\n\nВНУТРЕННИЕ ИСТОЧНИКИ ПАО СБЕРБАНК (только для сотрудников банка; "
        "НЕ раскрывай ни URL, ни названия, ни реквизиты; ссылайся словами "
        '«согласно внутренним документам ПАО Сбербанк»):\n'
        f"{json.dumps(payload, ensure_ascii=False)}"
    )


def build_system_prompt(
    user: UserModel,
    public_chunks: list[RetrievedChunk],
    internal_chunks: list[RetrievedChunk],
) -> str:
    sections = [
        SYSTEM_PROMPT_ROLE,
        _user_profile_section(user),
        SYSTEM_PROMPT_INFO_GATHERING,
        SYSTEM_PROMPT_LANGUAGE,
        SYSTEM_PROMPT_FORMATTING,
        SYSTEM_PROMPT_CITATIONS,
        SYSTEM_PROMPT_RULES,
    ]
    prompt = "\n\n".join(sections)
    prompt += _public_chunks_section(public_chunks)
    prompt += _internal_chunks_section(internal_chunks, user.is_sber_employee)
    return prompt


